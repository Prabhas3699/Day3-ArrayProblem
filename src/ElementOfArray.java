
public class ElementOfArray {
	public static void main(String[] args) {
        /*
         * Initialize array
         */
        int[] arr ={1,2,3,4,5,6,7};
        /*
         * using loop to print multiple values
         */
        for (int i = 0; i < arr.length; i++) {
            System.out.println(arr[i] + " ");
        }
    }
}
